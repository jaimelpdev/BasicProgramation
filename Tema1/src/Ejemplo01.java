
public class Ejemplo01 {

	public static void main(String[] args) {

		// Declaramos una variable entera llamada edad.
		int edad;

		// Inicializamos la variable edad.
		edad = 40;

		// Mostramos el valor de la variable edad.
		System.out.println(edad);

		// Declaramos una variable de cadena de caracteres llamada dni.
		String dni;

		// Inicializamos la variable dni.
		dni = "123435678G";
		
		// Mostramos el valor de la variable edad.
		System.out.println(dni);

	}

}
